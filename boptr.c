#include<stdio.h>

int main()
{

  int a=10,b=5;
  
  printf("a=%d,b=%d\n",a,b);
  printf("a+b=%d\n",a+b);
  printf("a-b=%d\n",a-b);
  printf("a*b=%d\n",a*b);
  printf("a/b=%d\n",a/b);
  printf("a%%b=%d\n",a%b);
  
  printf("a&b=%d\n",a&b);//bit-wise operators
  printf("a|b=%d\n",a|b);
  printf("a^b=%d\n",a^b);
  printf("~a=%d\n",~a);
  
  printf("a<<1=%d\n",a<<1);
  printf("a>>1=%d\n",a>>1);
  
  printf("a&&b=%d\n",a&&b);//logical or boolean  operators
  printf("a||b=%d\n",a||b);
  printf("!a=%d\n",!a);
  
  printf("a==b=%d\n",a==b);
  printf("a!=%d\n",a!=b);
  printf("a>b=%d\n",a>b);
  printf("a>=b=%d\n",a>=b);
  printf("a<b=%d\n",a<b);
  printf("a<=b=%d\n",a<=b);
  
  
}

